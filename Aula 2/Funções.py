def somar(num1, num2):
    resultado = num1 + num2
    print(resultado)
    return resultado

soma = somar(1, 10)
print(soma)

def junta_nome(primeiro_nome, ultimo_nome):
    nome_completo = primeiro_nome + " " + ultimo_nome
    return nome_completo
primeiro_nome = "Messias"
ultimo_nome = "De la Mancha"

print(junta_nome(primeiro_nome, ultimo_nome))

